            <br>* Dijamin oleh Lembaga Penjamin Simpanan (syarat dan ketentuan LPS).
            <br>* Bunga dapat ditransfer ke rekening Tabungan, Giro atau menambah pokok simpanan.
            <br>* Pada saat jatuh tempo dapat diperpanjang secara otomatis (Automatic Roll Over/ARO).
            <br>* Jangka waktu fleksibel yaitu 1, 3, 6, 12 atau 24 bulan.
