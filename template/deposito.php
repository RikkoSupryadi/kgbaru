            <h3 style="text-align: center;">DEPOSITO :<br>TUMBUH DENGAN TERENCANA</h3>
            <br>
            <br>
            <p style="color: #008080"><b>Deposito</b></p>
            <p>
              1.Deposito adalah simpanan yang pencairannya hanya dapat dilakukan pada &nbsp;&nbsp;&nbsp;jangka waktu tertentu dan syarat-syarat tertentu.<br>
              2.Deposito dapat dicairkan setelah jangka waktu berakhir.<br>
              3.Deposito yang akan jatuh tempo dapat diperpanjang secara otomatis &nbsp;&nbsp;&nbsp;(Automatic Roll Over).<br>
              4.Deposito dapat dalam mata uang rupiah maupun dalam mata uang asing.<br>
            </p>
            <p style="color: #008080"><b>Deposito Berjangka</b></p>
            <p>
              1.Merupakan simpanan yang pencairannya dilakukan berdasarkan jangka &nbsp;&nbsp;&nbsp;waktu tertentu.<br>
              2.Umumnya mempunyai jangka waktu mulai dari 1, 3, 6, dan 12 sampai &nbsp;&nbsp;&nbsp;dengan 24 bulan.<br>
              3.Diterbitkan dengan mencantumkan nama pemilik deposito baik &nbsp;&nbsp;&nbsp;perorangan maupun lembaga.<br>
              4.Kepada setiap deposan diberikan bunga yang besarnya dan waktu &nbsp;&nbsp;&nbsp;pembayarannya sesuai dengan yang berlaku di masing-masing bank.<br>
              5.Pembayaran bunga deposito dapat dilakukan setiap bulan atau setelah &nbsp;&nbsp;&nbsp;jatuh tempo sesuai jangka waktunya.<br>
              6.Pembayaran dapat dilakukan secara tunai maupun non tunai &nbsp;&nbsp;&nbsp;(pemindahbukuan).<br>
              7.Kepada setiap deposan dengan nominal deposito tertentu dikenakan &nbsp;&nbsp;&nbsp;pajak penghasilan dari bunga yang diterimanya.<br>
              8.Pencairan deposito sebelum jatuh tempo umumnya dikenakan denda.<br>
            </p>
            <p style="color: #008080"><b>Sertifikat Deposito</b></p>
            <p>
              1.Merupakan simpanan yang diterbitkan dengan janka waktu 1, 3, 6 dan 12 &nbsp;&nbsp;&nbsp;bulan.
            </p>
        </div>
        <div class="col-md-6" id="Right-Row" style="text-align: justify;">
          <p>
            2.Sertifikat Deposito diterbitkan atas unjuk dalam
            bentuk Sertifikat, tanpa &nbsp;&nbsp;&nbsp;mencantumkan nama
            pemilik deposito.<br>
            3.Sertifikat Deposito dapat diperjualbelikan kepada
            pihak lain.<br>
            4.Pembayaran bunga Sertifikat Deposito dapat
            dilakukan di muka, tiap bulan &nbsp;&nbsp;&nbsp;atau pada saat
            jatuh tempo, baik tunai maupun non tunai.<br>
          </p>
          <p style="color: #008080"><b>Keuntungan</b></p>
          <p>
            1.Dapat dijadikan jaminan kredit.<br>
            2.Memperoleh hasil bunga yang umumnya lebih
            tinggi dari bentuk &nbsp;&nbsp;&nbsp;simpanan lainnya.<br>
            3.Dapat mengelola keuangan secara lebih
            terencana sesuai dengan &nbsp;&nbsp;&nbsp;kebutuhan dan jangka
            waktu deposito.<br>
          </p>
          <p style="color: #008080"><b>Hal-hal Yang Harus Diperhatikan</b></p>
          <p>
            1.Pastikan Anda menerima bilyet/surat berharga
            (Deposito Berjangka atau &nbsp;&nbsp;&nbsp;Sertifikat Deposito).<br>
            2.Pada saat jatuh tempo, Anda berhak menerima
            pokok dan bunga &nbsp;&nbsp;&nbsp;deposito sesuai bunga yang
            berlaku setelah dipotong pajak.<br>
            3.Pada saat pencairan deposito, Anda
            berkewajiban untuk menandatangani &nbsp;&nbsp;&nbsp;formulir
            pencairan.<br>
            4.Perhatikan tingkat suku bunga deposito yang
            berlaku dan pastikan telah &nbsp;&nbsp;&nbsp;sesuai dengan
            ketentuan Lembaga Penjamin Simpanan (LPS).<br>
          </p>
