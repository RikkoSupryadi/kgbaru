            2. Kartu Keluarga<br>
            3. Keterangan penghasilan atau slip gaji.<br>
            4. Laporan keuangan (untuk wiraswasta)<br>
            5. NPWP Pribadi (untuk kredit di atas Rp. 100 juta)<br>
            6. SPT PPh Pribadi (untuk kredit di atas Rp. 50
            juta).<br>
            7. Foto kopi sertifikat induk dan atau pecahan (bila
            membelinya dari &nbsp;&nbsp;&nbsp;developer)<br>
            8. Foto kopi sertifikat (bila jual beli perorangan)
            9. Foto kopi IMB<br>
