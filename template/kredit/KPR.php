          <h3>Persyaratan KPR</h3>
          <p>
            Secara umum persyaratan dan ketentuan yang
            diperlakukan oleh bank untuk nasabah yang akan
            mengambil KPR relatif sama. Baik dari sisi
            administrasi maupun dari sisi penentuan kreditnya.
